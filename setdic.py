#set
class1={'amal','kamal','nimal','sunil'}
class2={'sunil','gihan','manik'}
comman=class1.intersection(class2) #ඡේදනය
print(comman)
diff=class1.difference(class2)# class1 ගේ පමනක් ඇති
print(diff)
union= class1.union(class2) # class1 & class 2 දෙකේම සිටින
print(union)




